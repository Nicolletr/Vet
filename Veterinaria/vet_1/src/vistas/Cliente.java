    private void buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarActionPerformed
        // TODO add your handling code here:
        Crud_Cliente crud = new Crud_Cliente();
        V_Cliente var = new V_Cliente();
        crud.mostrar(txtid.getText());
        txtid.setText(var.getId_cliente());
        txtcedula.setText(var.getC_i_());
        txtnombre.setText(var.getNombre());
        txtapellido.setText(var.getApellido());
        txtgenero.setText(var.getGenero());
        txtdireccion.setText(var.getDireccion());
        txttelefono.setText(var.getTelefono());
        txtregistro.setText(var.getFecha_registro());
        txtantiguedad.setText(var.getAntiguedad());
        txtvisita.setText(var.getUltima_visita());
        //txtmascotas.setText(var.getNumero_mascotas);

    }//GEN-LAST:event_buscarActionPerformed

