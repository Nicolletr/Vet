    private void modificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modificarActionPerformed
        // TODO add your handling code here:
        Crud_Cliente objcrud = new Crud_Cliente();
        V_Cliente objvar = new V_Cliente();
        objvar.setId_cliente(txtid.getText());
        objvar.setC_i_(txtcedula.getText());
        objvar.setNombre(txtnombre.getText());
        objvar.setApellido(txtapellido.getText());
        objvar.setGenero(txtgenero.getText());
        objvar.setDireccion(txtdireccion.getText());
        objvar.setTelefono(txttelefono.getText());
        objvar.setFecha_registro(txtregistro.getText());
        objvar.setAntiguedad(txtantiguedad.getText());
        objvar.setUltima_visita(txtvisita.getText());
        objvar.setNumero_mascotas(txtmascotas.getText());

        objcrud.actualizar(objvar.getId_cliente(),objvar.getC_i_(),objvar.getNombre(), objvar.getApellido(), objvar.getGenero(), objvar.getDireccion(), objvar.getTelefono(), objvar.getFecha_registro(), objvar.getAntiguedad(), objvar.getUltima_visita(), objvar.getNumero_mascotas());
        txtid.setText("");
        txtcedula.setText("");
        txtnombre.setText("");
        txtapellido.setText("");
        txtgenero.setText("");
        txtdireccion.setText("");
        txttelefono.setText("");
        txtregistro.setText("");
        txtantiguedad.setText("");
        txtvisita.setText("");
        txtmascotas.setText("");
    }//GEN-LAST:event_modificarActionPerformed

